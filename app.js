let next = document.querySelector('.next');
let prev = document.querySelector('.prev');
// let next = document.querySelector('.next');: Использует метод document.querySelector для нахождения элемента
//    с классом next (кнопка "вперёд") и сохраняет его в переменную next.
// let prev = document.querySelector('.prev');: Аналогично, ищет элемент с классом prev (кнопка "назад")
//     и сохраняет его в переменную prev.
// Эти переменные используются для управления событиями нажатия кнопок слайдера.

next.addEventListener('click', function (){
    let items = document.querySelectorAll('.item')
    document.querySelector('.slide').appendChild(items[0])
})
// next.addEventListener('click', function () { ... });: Добавляет обработчик события click к кнопке next.
//    Когда пользователь нажимает на кнопку, вызывается указанная функция.
// let items = document.querySelectorAll('.item');: Находит все элементы с классом item внутри слайдера и сохраняет их
//    в виде списка (NodeList).
// document.querySelector('.slide').appendChild(items[0]);: Берёт первый элемент (items[0]) из списка слайдов и
//    перемещает его в конец (appendChild). Это вызывает обновление отображения слайдов, смещая все слайды на один вперёд.
// Эта часть кода перемещает первый слайд в конец, создавая иллюзию пролистывания к следующему слайду.

prev.addEventListener('click', function (){
    let items = document.querySelectorAll('.item')
    document.querySelector('.slide').prepend(items[items.length - 1])
})
// prev.addEventListener('click', function () { ... });: Добавляет обработчик события click к кнопке prev.
//     При нажатии на эту кнопку вызывается функция, описанная ниже.
// let items = document.querySelectorAll('.item');: Опять же, находит все элементы с классом item внутри слайдера.
// document.querySelector('.slide').prepend(items[items.length - 1]);: Берёт последний элемент (items[items.length - 1])
//     из списка слайдов и перемещает его в начало (prepend). Это перемещение сдвигает слайды на один назад,
//     создавая иллюзию пролистывания к предыдущему слайду.
// Эта часть кода перемещает последний слайд в начало, позволяя пользователю вернуться к предыдущему слайду.
//
// Визуальное представление и объяснение работы кода:
// Слайдер: Основной контейнер <div class="slide"> содержит несколько элементов с классом item,
//     которые представляют собой отдельные слайды.
// Кнопки управления: Кнопки с классами prev и next используются для перемещения между слайдами.
// Переключение слайдов: При нажатии на кнопку "вперёд", первый слайд перемещается в конец списка,
//     а при нажатии на кнопку "назад" - последний слайд перемещается в начало. Это создаёт бесконечный цикл слайдов, где пользователь может перемещаться вперед и назад.
// Важные аспекты:
// Перемещение элементов DOM: appendChild и prepend — это методы, которые изменяют порядок элементов в DOM,
//     позволяя динамически менять отображение без перезагрузки страницы.
// querySelectorAll: Метод querySelectorAll возвращает статический NodeList (не обновляется при изменении DOM),
//     но в этом контексте он вызывается внутри каждой функции-обработчика, поэтому всегда получает актуальный список слайдов.
// Удобство и производительность: Хотя такие операции могут быть ресурсозатратными при большом количестве элементов,
//     они просты и интуитивно понятны для управления небольшим количеством слайдов.
// Возможные улучшения:
// Анимация переходов: Добавление CSS-анимаций или использования JavaScript-анимаций для плавного перехода между слайдами.
// Оптимизация: Избегание повторного вызова querySelectorAll внутри каждого обработчика и замена на сохранение состояния текущих слайдов.
